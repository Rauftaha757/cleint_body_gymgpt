class BodyPaths {
  /// SVG original size — scale your paths to match your widget size
  static const double svgWidth = 271.0;
  static const double svgHeight = 506.97;

  /// Returns scaled path. Call once and cache.
  static Path scalePath(Path p, Size widgetSize) {
    final sx = widgetSize.width / svgWidth;
    final sy = widgetSize.height / svgHeight;
    return p.transform(Matrix4.diagonal3Values(sx, sy, 1).storage);
  }

  /// Cache for all paths - extracted from RPSCustomPainter
  static List<Path>? _cachedPaths;

  /// Get all paths by extracting them from only_paths.dart
  /// This method builds all 120 paths from the RPSCustomPainter
  static List<Path> getAllPaths() {
    if (_cachedPaths != null) return _cachedPaths!;

    _cachedPaths = _buildAllPaths();
    return _cachedPaths!;
  }

  /// Build all paths from only_paths.dart
  /// Extracts path definitions from RPSCustomPainter.paint()
  static List<Path> _buildAllPaths() {
    final paths = <Path>[];

    // Extract all paths from only_paths.dart
    // Path definitions are copied from RPSCustomPainter.paint() method
    // This creates all 120 paths (path_0 through path_120)

    final extractor = _PathBuilder();
    extractor.buildPaths(paths);

    return paths;
  }

  /// Get path by index (0-119)
  static Path? getPathByIndex(int index) {
    final allPaths = getAllPaths();
    if (index < 0 || index >= allPaths.length) return null;
    return allPaths[index];
  }

  /// Hit test — returns index of tapped path or null
  static int? hitTest(Offset tap, Size widgetSize) {
    final sx = widgetSize.width / svgWidth;
    final sy = widgetSize.height / svgHeight;
    // Scale tap back to SVG coordinates
    final svgTap = Offset(tap.dx / sx, tap.dy / sy);

    final allPaths = getAllPaths();
    // Test paths in reverse order (last drawn = top layer)
    for (int i = allPaths.length - 1; i >= 0; i--) {
      if (allPaths[i].contains(svgTap)) return i;
    }
    return null;
  }
}

/// Helper class to build paths from only_paths.dart
class _PathBuilder {
  void buildPaths(List<Path> output) {
    // Use RPSCustomPainter to extract paths
    // We create a custom painter that captures paths instead of drawing
    final pathCapture = _PathCapturePainter();
    pathCapture.capturePaths(output);
  }
}

