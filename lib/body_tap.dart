  void _handleTap(TapDownDetails details) {
    final box = context.findRenderObject() as RenderBox;
    final local = box.globalToLocal(details.globalPosition);
    final tappedIndex = BodyPaths.hitTest(local, box.size);

    if (tappedIndex != null) {
      setState(() {
        final muscleName = _muscleNames[tappedIndex];
        final groupPaths = muscleName != null
            ? _muscleGroups[muscleName]
            : null;

        if (groupPaths != null && groupPaths.isNotEmpty) {
          // If this path belongs to a group, select/deselect all paths in the group
          final isGroupSelected = groupPaths.every(
            (index) => _selected.contains(index),
          );
          if (isGroupSelected) {
            _selected.removeAll(groupPaths);
          } else {
            _selected.addAll(groupPaths);
          }
        } else {
          // No group, just toggle this single path
          _selected.contains(tappedIndex)
              ? _selected.remove(tappedIndex)
              : _selected.add(tappedIndex);
        }

        // Update last tapped muscle
        _lastTappedMuscle = muscleName ?? 'muscle_$tappedIndex';
      });

      // 🔑 Fire callback with index + name
      final muscleName = _muscleNames[tappedIndex] ?? 'muscle_$tappedIndex';
      widget.onMuscleTapped?.call(tappedIndex, muscleName);
    }
  }

  @override
  Widget build(BuildContext context) {
    return Stack(
      children: [
        GestureDetector(
          onTapDown: _handleTap,
          child: CustomPaint(
            painter: BodyPainter(Set<int>.from(_selected)),
            size: const Size(271, 506.97),
          ),
        ),
        if (_lastTappedMuscle != null)
          Positioned(
